package JavaExperimentation;
//Program to convert the value of Farenheit to Centigrade
class Farenheit

{
    public static void main(String[] args)
    
    {
        int Farenheit=70;
        
        float Centigrade;
        
        Centigrade=(Farenheit - 32)*5/9;
        
        System.out.println("The conversion value is : " + Centigrade);
        
    }
    
}    
        
      
        